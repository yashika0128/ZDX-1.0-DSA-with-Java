package Lecture04_Variables_Datatypes.DataTypes;

public class q1_all_primitive_DT {
    public static void main(String[] args) {
        byte b=100;
        short s=2000;
        int i=100000;
        long l=9876543210L;
        float f=3.14f;
        double d=99.99;
        char c='A';
        boolean flag=true;

        System.out.println(b+" "+s+" "+i+" "+l);
        System.out.println(f+" "+d+" "+c+" "+flag);
    }
}
